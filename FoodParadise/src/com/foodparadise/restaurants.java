package com.foodparadise;

public class restaurants {
	private String restaurant_id;
	private String restaurant_name;
	private String restaurant_address;
	private String restaurant_lat;
	private String restaurant_lon;
	private String restaurant_rating;
	private String restaurant_rating_num;
	private String restaurant_checkin_num;

	public String getRestaurant_id() {
		return restaurant_id;
	}
	public void setRestaurant_id(String restaurant_id) {
		this.restaurant_id = restaurant_id;
	}
	public String getRestaurant_address() {
		return restaurant_address;
	}
	public void setRestaurant_address(String restaurant_address) {
		this.restaurant_address = restaurant_address;
	}
	public String getRestaurant_name() {
		return restaurant_name;
	}
	public void setRestaurant_name(String restaurant_name) {
		this.restaurant_name = restaurant_name;
	}
	public String getRestaurant_lon() {
		return restaurant_lon;
	}
	public void setRestaurant_lon(String restaurant_lon) {
		this.restaurant_lon = restaurant_lon;
	}
	public String getRestaurant_lat() {
		return restaurant_lat;
	}
	public void setRestaurant_lat(String restaurant_lat) {
		this.restaurant_lat = restaurant_lat;
	}
	public String getRestaurant_rating() {
		return restaurant_rating;
	}
	public void setRestaurant_rating(String restaurant_rating) {
		this.restaurant_rating = restaurant_rating;
	}
	public String getRestaurant_rating_num() {
		return restaurant_rating_num;
	}
	public void setRestaurant_rating_num(String restaurant_rating_num) {
		this.restaurant_rating_num = restaurant_rating_num;
	}
	public String getRestaurant_checkin_num() {
		return restaurant_checkin_num;
	}
	public void setRestaurant_checkin_num(String restaurant_checkin_num) {
		this.restaurant_checkin_num = restaurant_checkin_num;
	}
}
